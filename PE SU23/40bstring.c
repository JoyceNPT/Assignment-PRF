#include <stdio.h>

int main(){
    int w, h, i, j;
    scanf("%d%d", &w, &h);

    for(i = 0; i < h; i++){
        for (int j = 0; j < h - i - 1; j++) {
            printf("  ");
        }

        for (int j = 0; j < w; j++) {
            printf("* ");
        }
        printf("\n");
    }

    
    
    return 0;
}
 
//  i = 0
//  j = 0; j < h - i - 1 ==> 0 < 5 - 0 - 1 ==> 0 < 4 (" ")
//  j = 0; j < w         ==>               ==> 0 < 6 ("* ")

//  j = 1; j < h - i - 1 ==> 0 < 5 - 0 - 1 ==> 0 < 4 (" ")
//  j = 1; j < w         ==>               ==> 1 < 6 ("* ")
//  ...

